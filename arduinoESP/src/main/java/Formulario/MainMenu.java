    public void toogleDoorImage(){
    if(isDoorOpen){
        lblDoorImage.setIcon(closeDoorImage);
        LblEstado.setText("Puerta Cerrada");
    }else{
        lblDoorImage.setIcon(OpenDoorImage);
        LblEstado.setText("Puerta Abierta");
    }
    isDoorOpen = !isDoorOpen; 
    }
    
    public void dateUpdate(){
    DateTimeFormatter formater = DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss");
      
           while(true){
               try{
               Thread.sleep(5000);
               LblDate.setText(formater.format(LocalDateTime.now()));
               }catch(InterruptedException ex){
                   ex.printStackTrace();
               }
           }
    }
    
    public void updateDate(){
    LblDate.setText(DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss").format(LocalDateTime.now()));
    }
    
    private void startServer() {
        int port = 12345;
        try {
            serverSocket = new ServerSocket(port);
            System.out.println("Servidor escuchando en el puerto " + port);

            while (true) {
                try (Socket clientSocket = serverSocket.accept();
                     PrintWriter out = new PrintWriter(clientSocket.getOutputStream(), true);
                     BufferedReader in = new BufferedReader(new InputStreamReader(clientSocket.getInputStream()))) {

                    String request = in.readLine();
                    System.out.println("Petici√≥n recibida: " + request);

                    if ("openDoor".equals(request)) {
                        UserRegister userRegister = new UserRegister();
                        toogleDoorImage();
                        updateDate();
                        userRegister.sendDatas(true,  DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss").format(LocalDateTime.now()));
                        out.println("Puerta abierta");
                    } else if ("closeDoor".equals(request)) {
                        UserRegister userRegister = new UserRegister();
                        toogleDoorImage();
                        updateDate();
                        userRegister.sendDatas(true,  DateTimeFormatter.ofPattern("dd/MM/yyyy HH:mm:ss").format(LocalDateTime.now()));
                        out.println("Puerta cerrada");
                    } else {
                        out.println("Comando desconocido");
                    }
                } catch (IOException e) {
                    e.printStackTrace();
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }
    }
    

    
